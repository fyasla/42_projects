int	ft_atoi(const char *str)
{
	int sg;
	int	res;

	sg = 1;
	res = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r' ||
	*str == '\v' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '-' || *str == '+')
	{
		sg = (44 - *str);
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = 10 * res + (*str - '0');
		str++;
	}
	return (sg * res);
}
